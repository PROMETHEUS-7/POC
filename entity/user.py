class User:
    def __init__(self, user_id=None, name=None, username=None, password=None, email=None, designation=None, role=None, strike=None):
        self.user_id = user_id
        self.name = name
        self.username = username
        self.password = password
        self.email = email
        self.designation = designation
        self.role = role
        self.strike = strike
